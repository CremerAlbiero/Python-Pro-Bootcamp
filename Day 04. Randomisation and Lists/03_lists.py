# how to create a related list, but separate it. 
# Example: fruits and vegetables

fruits = ["apple", "banana", "orange", "strawberry", "kiwi", "mango", "grape", "watermelon", "cherry", "blueberry"]
vegetables = ["carrot", "broccoli", "cucumber", "bell pepper", "spinach"]
concatenated_list = [fruits, vegetables]
print(concatenated_list)
 
print(concatenated_list[1][1])   #prints out the second list within "concatened list" and picks up the element at index[1].    